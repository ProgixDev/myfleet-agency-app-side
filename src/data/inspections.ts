import type { Inspection, CapturedPhoto, PhotoAngle } from '@/types/inspection';
import { PHOTO_ANGLES } from '@/types/inspection';

function makePhotos(damages: Record<number, { count: number }>): CapturedPhoto[] {
  return PHOTO_ANGLES.map((a, i) => {
    const d = damages[i];
    return {
      angle: a.key,
      uri: '',
      timestamp: '2026-04-07T10:00:00',
      aiResult: d
        ? {
            damagesFound: d.count,
            markers: Array.from({ length: d.count }, (_, j) => ({
              x: 0.3 + j * 0.2,
              y: 0.4 + j * 0.1,
              confidence: 0.85 + Math.random() * 0.1,
            })),
          }
        : { damagesFound: 0, markers: [] },
      annotations: [],
    };
  });
}

