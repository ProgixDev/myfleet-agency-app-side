import type { Vehicle } from '@/types/vehicle';
import { vehicleAssets } from './vehicleAssets';

function buildMedia(slug: string) {
  const assets = vehicleAssets[slug];
  if (assets && assets.photos.length > 0) {
    return {
      photos: assets.photos,
      videos: [],          // videos loaded at runtime, not bundled
      hasVideo: assets.hasVideo,
      thumbnail: assets.thumbnail,
      videoPaths: assets.videoPaths,
    };
  }
  // New vehicles without local assets — vehicleImages.ts aliases them
  // to similar existing vehicles' photos at display time
  return {
    photos: [],
    videos: [],
    hasVideo: false,
    thumbnail: null,
    videoPaths: [] as string[],
  };
}

